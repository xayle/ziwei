def get_brightness(
    star: str,
    branch_idx: int,
    brightness_method: str = "standard",
) -> tuple[int, str]:
    """返回 (亮度值, 亮度名称)

    brightness_method:
        "standard"  依据斗数全书（默认）
        "zhongzhou" 中州派
        "mod1"      现代修订一
        "mod2"      现代修订二
    """
    overrides = _BRIGHTNESS_OVERRIDES.get(brightness_method, {})
    key = (star, branch_idx % 12)
    val = overrides.get(key, BRIGHTNESS_TABLE.get(key, 2))
    return val, BRIGHTNESS_NAMES[val]


