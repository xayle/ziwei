def get_aux_brightness(star: str, branch_idx: int) -> tuple[int, str]:
    """返回辅星的 (亮度值, 亮度名称)。

    有亮度表的星按表查；天魁天钺左辅右弼等固定亮度；
    其余辅星无宫位差异，返回 "平"(2)。
    """
    if star in _AUX_FIXED_BRIGHTNESS:
        val = _AUX_FIXED_BRIGHTNESS[star]
        return val, BRIGHTNESS_NAMES[val]
    key = (star, branch_idx % 12)
    if key in AUX_BRIGHTNESS_TABLE:
        val = AUX_BRIGHTNESS_TABLE[key]
        return val, BRIGHTNESS_NAMES[val]
    return 2, "平"
