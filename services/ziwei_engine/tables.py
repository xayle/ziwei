# ─────────────────────────────────────────────────────
# 时支对应表 (时辰名 → 地支索引)
# 子(0) 丑(1) 寅(2) 卯(3) 辰(4) 巳(5) 午(6) 未(7) 申(8) 酉(9) 戌(10) 亥(11)
# ─────────────────────────────────────────────────────
def hour_to_branch(hour: int, minute: int = 0) -> int:
    """
    将24小时制时间转为时辰地支索引 (子=0…亥=11)。
    子时: 23:00-01:00 (跨日子时统一按"晚子时"处理)
    """
    total_min = hour * 60 + minute
    if total_min < 60:    return 0   # 00:00-00:59 = 子
    elif total_min < 180:  return 1   # 01:00-02:59 = 丑
    elif total_min < 300:  return 2   # 03:00-04:59 = 寅
    elif total_min < 420:  return 3   # 05:00-06:59 = 卯
    elif total_min < 540:  return 4   # 07:00-08:59 = 辰
    elif total_min < 660:  return 5   # 09:00-10:59 = 巳
    elif total_min < 780:  return 6   # 11:00-12:59 = 午
    elif total_min < 900:  return 7   # 13:00-14:59 = 未
    elif total_min < 1020: return 8   # 15:00-16:59 = 申
    elif total_min < 1140: return 9   # 17:00-18:59 = 酉
    elif total_min < 1260: return 10  # 19:00-20:59 = 戌
    elif total_min < 1380: return 11  # 21:00-22:59 = 亥
    else:                  return 0   # 23:00-23:59 = 子

