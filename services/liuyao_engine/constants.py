# 六亲关系: 我(宫五行) vs 爻五行
def get_liuqin(wo: str, yao_wx: str) -> str:
    """我生→子孙  生我→父母  克我→官鬼  我克→妻财  同→兄弟"""
    if wo == yao_wx:
        return "兄弟"
    if yao_wx == WUXING_SHENG.get(wo, ""):
        return "子孙"
    if wo == WUXING_SHENG.get(yao_wx, ""):
        return "父母"
    if wo in WUXING_KE and WUXING_KE[wo] == yao_wx:
        return "妻财"
    return "官鬼"
